longNumber = 908090800
cmplx = 2.0+2.3j
print("The values are : ")
print("number is",number,", decimal is",decimal,", longNumber is",longNumber," & cmplx is",cmplx)
print()
print("The object types are :")
print("The type of number is",type(number))
print("The type of decimal is",type(decimal))
print("the type of longNumber is",type(longNumber))
print("The type of cmplx is",type(cmplx))
print()
